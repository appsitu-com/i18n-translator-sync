function canonicalizeLocaleTag(locale: string): string {
  const trimmed = locale.trim()
  if (!trimmed) return trimmed
  const parts = trimmed.replace(/_/g, '-').split('-').filter((part) => part.length > 0)
  if (parts.length === 0) return ''
  const [language, ...rest] = parts
  const canonicalRest = rest.map((part) => {
    if (part.length === 4) return `${part.charAt(0).toUpperCase()}${part.slice(1).toLowerCase()}`
    if (part.length <= 3) return part.toUpperCase()
    return part
  })
  return [language.toLowerCase(), ...canonicalRest].join('-')
}

function normalizeLocaleKey(locale: string): string {
  return canonicalizeLocaleTag(locale).toLowerCase()
}

function createNormalizedLangMap(langMap: MateCatLangMap): MateCatLangMap {
  const normalized: MateCatLangMap = {}
  for (const [sourceLocale, mappedLocale] of Object.entries(langMap)) {
    const sourceKey = normalizeLocaleKey(sourceLocale)
    const targetValue = canonicalizeLocaleTag(mappedLocale)
    if (!sourceKey || !targetValue) continue
    normalized[sourceKey] = targetValue
  }
  return normalized
}

export function resolveMateCatLocale(locale: string, langMap: MateCatLangMap = {}): string {
  const normalizedInput = normalizeLocaleKey(locale)
  const normalizedUserLangMap = createNormalizedLangMap(langMap)
  const userMappedLocale = normalizedUserLangMap[normalizedInput]
  if (userMappedLocale) return userMappedLocale
  const defaultMappedLocale = DEFAULT_MATECAT_LANG_MAP[normalizedInput]
  if (defaultMappedLocale) return defaultMappedLocale
  return canonicalizeLocaleTag(locale)
}
